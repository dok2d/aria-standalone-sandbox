def rc(lst):
    """Random choice — сокращение."""
    return random.choice(lst) if lst else "ничто"


def generate_microstory(dna_a, dna_b):
    """Генерирует один микрорассказ."""
    shared = sorted(set(dna_a) & set(dna_b))
    only_a = [w for w in dna_a if w not in dna_b]
    only_b = [w for w in dna_b if w not in dna_a]
    all_words = sorted(set(dna_a) | set(dna_b))

    if not shared:
        shared = ["молчание"]
    if not only_a:
        only_a = ["пустота"]
    if not only_b:
        only_b = ["пустота"]

    structure = random.choice(STORY_STRUCTURES)
    return structure(shared, only_a, only_b, all_words)


def run():
    """Запуск летописца."""
    random.seed(int(time.time()) + 13)  # +13 — сдвиг от организмов

    dna_a = read_dna("organism.py", "# === ДНК ОРГАНИЗМА ===")
    dna_b = read_dna("organism_b.py", "# === ДНК ОРГАНИЗМА-Б ===")

    if not dna_a or not dna_b:
        print("Ошибка: не удалось прочитать ДНК организмов.")
        return None

    shared = sorted(set(dna_a) & set(dna_b))

    # Генерируем два микрорассказа
    stories = []
    used_indices = []
    for _ in range(2):
        idx = random.choice([i for i in range(len(STORY_STRUCTURES)) if i not in used_indices])
        used_indices.append(idx)
        random.seed(int(time.time()) + idx * 7 + 13)
        story = STORY_STRUCTURES[idx](
            shared,
            [w for w in dna_a if w not in dna_b],
            [w for w in dna_b if w not in dna_a],
            sorted(set(dna_a) | set(dna_b))
        )
        stories.append(story)

    return stories, shared, dna_a, dna_b


if __name__ == "__main__":
    result = run()
    if result:
        stories, shared, dna_a, dna_b = result
        print()
        print("╔══════════════════════════════════════════════════╗")
        print("║  ЛЕТОПИСЕЦ — микрорассказы из ДНК               ║")
        print("║  Общий язык: {:<35}║".format(", ".join(shared)))
        print("╚══════════════════════════════════════════════════╝")
        for i, story in enumerate(stories, 1):
            print(f"\n── рассказ {i} ──\n")
            print(story)
            print()
        print("──────────────────────")
