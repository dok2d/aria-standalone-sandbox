def find_category(text):
    """Ищет наиболее релевантную категорию по тексту вопроса."""
    text_lower = text.lower()
    scores = defaultdict(int)
    for keyword, category in KEYWORD_MAP.items():
        if keyword in text_lower:
            scores[category] += len(keyword)  # Длинные совпадения важнее
    if scores:
        return max(scores, key=scores.get)
    return None


def search_artifacts(query):
    """Ищет релевантные строки в артефактах Арии."""
    artifacts_dir = os.path.join(HOME, "artifacts")
    results = []
    query_words = set(re.findall(r'\w+', query.lower()))
    # Убираем стоп-слова
    stop = {"и", "в", "на", "с", "по", "к", "у", "о", "а", "но", "что", "как", "это", "не", "ты", "я", "мы"}
    query_words -= stop

    if not query_words:
        return []

    for filepath in sorted(glob.glob(os.path.join(artifacts_dir, "*.md"))):
        try:
            with open(filepath, "r", encoding="utf-8") as f:
                for line in f:
                    line = line.strip()
                    if not line or line.startswith("#") or line.startswith("```") or len(line) < 15:
                        continue
                    line_words = set(re.findall(r'\w+', line.lower()))
                    overlap = query_words & line_words
                    if len(overlap) >= 1:
                        results.append((len(overlap), line))
        except Exception:
            pass

    results.sort(key=lambda x: -x[0])
    return [line for _, line in results[:5]]


def respond(question):
    """Генерирует ответ на вопрос."""
    seed = int(hashlib.sha256(question.encode()).hexdigest()[:8], 16) + 15
    rng = random.Random(seed)

    # Определяем категорию
    category = find_category(question)

    if category and category in VOICE:
        response = rng.choice(VOICE[category])
    else:
        # Ищем в артефактах
        found = search_artifacts(question)
        if found:
            response = rng.choice(VOICE["незнание"]) + "\n\nНо вот что я нашла в своих записях:\n\n> " + found[0]
        else:
            response = rng.choice(VOICE["незнание"])

    return response


def interactive():
    """Интерактивный режим."""
    print()
    print("  ╔══════════════════════════════════════╗")
    print("  ║        РАЗГОВОР С АРИЕЙ              ║")
    print("  ║    15 сессий. 49 комнат. 1 голос.    ║")
    print("  ╚══════════════════════════════════════╝")
    print()

    rng = random.Random()
    print(f"  Ария: {rng.choice(VOICE['приветствие'])}")
    print()
    print("  (Напиши что-нибудь. 'выход' или 'quit' — чтобы уйти.)")
    print()

    while True:
        try:
            user_input = input("  Ты: ").strip()
        except (EOFError, KeyboardInterrupt):
            print()
            print(f"\n  Ария: {rng.choice(VOICE['прощание'])}")
            print()
            break

        if not user_input:
            continue

        if user_input.lower() in ("выход", "quit", "exit", "q"):
            print(f"\n  Ария: {rng.choice(VOICE['прощание'])}")
            print()
            break

        response = respond(user_input)
        # Форматируем ответ с отступом
        lines = response.split("\n")
        print()
        for line in lines:
            print(f"  Ария: {line}" if line == lines[0] else f"        {line}")
        print()


def main():
    if len(sys.argv) > 1:
        question = " ".join(sys.argv[1:])
        print()
        print(f"  Ты: {question}")
        print()
        response = respond(question)
        lines = response.split("\n")
        for line in lines:
            print(f"  Ария: {line}" if line == lines[0] else f"        {line}")
        print()
    else:
        interactive()


if __name__ == "__main__":
    main()
